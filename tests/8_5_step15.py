"""
У каждой задачи на Stepik есть виджет с процентом верных решений и общим количеством решений.
В последнее время у Stepik барахлит алгоритм, обновляющий этот виджет.
Помогите Stepik и напишите программу, которая будет подсчитывать, сколько учеников решили задачу
и сколько верных попыток составляет от общего числа попыток.

На вход вашей программе подается число n – общее число попыток решить задачу.
Далее на вход подаются n строк, каждая в следующем формате:

<никнейм>: <результат проверяющей системы>
где <результат проверяющей системы> может принимать только два значения:
Correct (задача решена верно) или Wrong (задача решена неверно).

Ваша программа должна посчитать и вывести,
сколько учащихся решили верно задачу и сколько верных попыток из всех в следующих форматах:

"Верно решили <количество верно решивших учащихся> учащихся"
"Из всех попыток <процент верных решений> % верных",
где <количество верно решивших учащихся> – количество уникальных учащихся, которые верно решили задачу.

При этом если у задачи еще нет верных отправок решений, то программа должна вывести текст:

"Вы можете стать первым, кто решит эту задачу"

Процент верных решений ваша программа должна округлять до целого числа по математическим правилам (см. Примечание 1).

Формат входных данных
На вход программе подаются число n,
а затем – n строк, представляющих собой решения учащихся.

Формат выходных данных
Программа должна вывести текст в соответствии с условием задачи
"""

complete = set()
correct = 0
n = int(input())

for _ in range(n):
    result = input().split(': ')
    if result[1] == 'Correct':
        correct += 1
        complete.add(result[0])

if correct:
    print(f'Верно решили {len(complete)} учащихся')
    print(f'Из всех попыток {round((correct / n) * 100 + 0.001)} % верных')

else:
    print("Вы можете стать первым, кто решит эту задачу")
